package br.com.agrego.poc.utils;

public class MyProperty {

	private final String key;
	private final String value;
	
	public MyProperty(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}
	
	
	
}
